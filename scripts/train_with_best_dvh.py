from __future__ import annotations

import argparse
import json
import os
import sys
from pathlib import Path

import pytorch_lightning as pl

REPO_ROOT = Path(__file__).resolve().parents[1]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

import DosePrediction.Train.config as config
from DosePrediction.Train.train_light_pyfer import OpenKBPDataModule, Pyfer, build_logger
from DosePrediction.utils.runtime import get_lightning_accelerator


def resolve_best_json(candidate_path: str) -> Path:
    candidate = Path(candidate_path)
    if candidate.exists():
        return candidate

    fallback_candidates = [
        Path("runs/DosePrediction/dvh_tuning_stage2/dvh_tuning_results.json"),
        Path("runs/DosePrediction/dvh_tuning_stage1/dvh_tuning_results.json"),
    ]
    for path in fallback_candidates:
        if path.exists():
            print(f"[INFO] best-json not found at '{candidate}'. Fallback to '{path}'.")
            return path

    discovered = sorted(Path("runs").glob("**/dvh_tuning_results.json"), key=lambda p: p.stat().st_mtime, reverse=True)
    if discovered:
        print(f"[INFO] best-json not found at '{candidate}'. Fallback to latest '{discovered[0]}'.")
        return discovered[0]

    raise FileNotFoundError(
        f"Best tuning summary not found: {candidate}\n"
        f"Please run DVH tuning first, for example:\n"
        f"  python scripts/tune_dvh_hparams.py --max-epochs 20 --output-root runs/DosePrediction/dvh_tuning_stage1\n"
        f"  python scripts/tune_dvh_hparams.py --max-epochs 35 --output-root runs/DosePrediction/dvh_tuning_stage2"
    )


def load_best_config(summary_path: Path) -> dict:
    payload = json.loads(summary_path.read_text(encoding="utf-8"))
    best = payload.get("best")
    if not best:
        raise ValueError(f"Missing 'best' entry in {summary_path}")
    return best


def build_model_config(best: dict) -> dict:
    return {
        "act": best.get("act", "mish"),
        "multiS_conv": bool(best.get("multiS_conv", True)),
        "lr": float(best.get("lr", 0.0006130697604327541)),
        "weight_decay": float(best.get("weight_decay", 0.00016303111017674179)),
        "delta1": float(best.get("delta1", 10.0)),
        "delta2": float(best.get("delta2", 8.0)),
        "hotspot_weight": float(best.get("hotspot_weight", 0.75)),
        "hotspot_quantile": float(best.get("hotspot_quantile", 0.98)),
        "coldspot_weight": float(best.get("coldspot_weight", 0.35)),
        "coldspot_quantile": float(best.get("coldspot_quantile", 0.10)),
    }


def run_training(model_cfg: dict, max_epochs: int, freeze: bool, ckpt_dir: Path, run_name: str) -> None:
    data = OpenKBPDataModule()
    model = Pyfer(model_cfg, freeze=freeze)

    accelerator, devices = get_lightning_accelerator()
    trainer = pl.Trainer(
        devices=devices,
        accelerator=accelerator,
        max_epochs=max_epochs,
        check_val_every_n_epoch=model.check_val,
        logger=build_logger(run_name=run_name),
        default_root_dir=str(ckpt_dir),
    )
    trainer.fit(model, datamodule=data)


def main() -> None:
    parser = argparse.ArgumentParser(description="Load best DVH tuning params and launch full training.")
    parser.add_argument(
        "--best-json",
        default="runs/DosePrediction/dvh_tuning_stage2/dvh_tuning_results.json",
        help="Path to tuning summary JSON generated by tune_dvh_hparams.py",
    )
    parser.add_argument("--freeze-epochs", type=int, default=300)
    parser.add_argument("--finetune-epochs", type=int, default=120)
    parser.add_argument("--skip-finetune", action="store_true")
    parser.add_argument("--output-dir", default=config.CHECKPOINT_MODEL_DIR_FINAL)
    args = parser.parse_args()

    best_path = resolve_best_json(args.best_json)

    os.makedirs(args.output_dir, exist_ok=True)
    best_config = load_best_config(best_path)
    model_cfg = build_model_config(best_config)

    print("Using best config:")
    print(json.dumps(model_cfg, indent=2, ensure_ascii=False))

    run_training(
        model_cfg=model_cfg,
        max_epochs=args.freeze_epochs,
        freeze=True,
        ckpt_dir=Path(args.output_dir) / "freeze_stage",
        run_name="best_dvh_freeze_stage",
    )

    if not args.skip_finetune:
        run_training(
            model_cfg=model_cfg,
            max_epochs=args.finetune_epochs,
            freeze=False,
            ckpt_dir=Path(args.output_dir) / "finetune_stage",
            run_name="best_dvh_finetune_stage",
        )


if __name__ == "__main__":
    main()
